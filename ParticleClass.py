
class Particle:
    def __init__(self, velocity, angle, kineticE, potentialE, mass, pos, momentum, collFlag, radius,density):
        self.self = self
        self.velocity = velocity
        self.angle = angle
        self.kineticE = kineticE
        self.potentialE = potentialE
        self.mass = mass
        self.pos = pos
        self.momentum = momentum
        self.collFlag = collFlag
        self.radius = radius
        self.density = density

